package FlyWeightDesignPattern;

public class NotebookStaticAttributes {
    private final int height = 10;
    private final int width = 10;
    private final String publication = "VSP";

    public int getHeight() {
        return height;
    }
    public int getWidth() {
        return width;
    }
    public String getPublication() {
        return publication;
    }
}
